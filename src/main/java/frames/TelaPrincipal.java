  public void configurarPermissoes(){
    if (Sessao.tipoUsuario.equalsIgnoreCase("usuário")) {
        btnExcluirProjetos.setEnabled(false);
        btnExcluirReceitas.setEnabled(false);
    }
    if (Sessao.tipoUsuario.equalsIgnoreCase("visitante")) {
        btnExcluirReceitas.setEnabled(false);
        btnExcluirProjetos.setEnabled(false);
        btnExcluirProjetos.setEnabled(false);
        btnExcluirReceitas.setEnabled(false);
    }
  }

