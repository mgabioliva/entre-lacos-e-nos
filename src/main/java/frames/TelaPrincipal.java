  private void preencheTabelaProjetosHome(List<Projeto> projeto){
    String columns[] = {"id","nome", "categoria", "duração", "material", "status", "usuário", "id receita"};
    String dados[][] = new String[projeto.size()][columns.length];
    
    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm:ss");
    
    int i=0;
    for(Projeto d: projeto){
      dados[i] = new String[]{
        
        String.valueOf(d.getId_projeto()),
        d.getNome(),
        d.getCategoria(),
        d.getDuracao().format(formatter),
        d.getMaterial(),
        d.getStatus(),
        d.getUsuario().getUsername(),
        String.valueOf(d.getReceita().getId_receita())};
      i++;
    }
    DefaultTableModel model = new DefaultTableModel(dados, columns);
    tblProjetosHome.setModel(model);
  }  
  private void atualizarTabelaProjetosHome() {
    ListaProjeto listaProjeto = new ListaProjeto();
    List<Projeto> projetos = listaProjeto.listar();
    preencheTabelaProjetosHome(projetos);
  }
  
