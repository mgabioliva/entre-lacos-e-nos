  private void btnExcluirProjetosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProjetosActionPerformed
   try{
      
      if(tblProjetos.getSelectedRow() >= 0){String id = (String)tblProjetos.getValueAt(tblProjetos.getSelectedRow(), 0);
       int resposta = JOptionPane.showConfirmDialog(this, "deseja mesmo excluir o projeto " + id + "?");
       
       if(resposta == 0){
         ListaProjeto listaProjetos = new ListaProjeto();
         listaProjetos.excluir(Integer.parseInt(id));
         JOptionPane. showMessageDialog(this, "projeto excluído com sucesso!");
         atualizarTabelaProjetos();

       }
      }
    }
    catch(Exception e){
      JOptionPane.showMessageDialog(this, "ocorreu um erro!\n Verifique os dados e tente novamente!");
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_btnExcluirProjetosActionPerformed

