  private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
    try{
      
      if (txtNome.getText().isEmpty() ||txtCategoria.getText().isEmpty() ||ftxtDuracao.getText().trim().isEmpty() || cmbStatus.getSelectedItem() == null ||
       txtMaterial.getText().isEmpty() || txtDetalhes.getText().isEmpty() || cmbUsuario.getSelectedItem() == null ||cmbReceita.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "preencha todos os campos. crie pelo menos uma receita antes de criar um nov projeto.");
        return;
      }
      
      Projeto novoProjeto = new Projeto();
      
      novoProjeto.setNome(txtNome.getText());
      novoProjeto.setCategoria(txtCategoria.getText());
      novoProjeto.setStatus(cmbStatus.getSelectedItem().toString());
      novoProjeto.setMaterial(txtMaterial.getText());
      novoProjeto.setDetalhes(txtDetalhes.getText());
      Usuario usuarioSelecionado = (Usuario) cmbUsuario.getSelectedItem();
      novoProjeto.setUsuario(usuarioSelecionado);
      Receita receitaSelecionada = (Receita) cmbReceita.getSelectedItem();
      novoProjeto.setReceita(receitaSelecionada);
      
      LocalTime duracao;
      try {
        duracao = LocalTime.parse(ftxtDuracao.getText(),DateTimeFormatter.ofPattern("HH:mm:ss"));
      }
      catch (Exception e) {
        JOptionPane.showMessageDialog(this, "duração inválida!\n ex: (01:20:50)");
        return;
      }
      novoProjeto.setDuracao(duracao);
      
      ListaProjeto listaProjeto = new ListaProjeto();
      listaProjeto.cadastrar(novoProjeto);
      JOptionPane.showMessageDialog(this, "projeto cadastrado com sucesso!" );
      this.dispose();
    }
    catch(Exception e){
      JOptionPane.showMessageDialog(this, "ocorreu uma falha:\n" + e.getMessage());
    }
  }//GEN-LAST:event_btnCadastrarActionPerformed

