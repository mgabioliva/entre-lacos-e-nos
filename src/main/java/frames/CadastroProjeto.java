  private void preencheComboUsuario(){
    ListaUsuario listaUsuario = new ListaUsuario();
    List<Usuario> usuarios = listaUsuario.listar();
    
    for(Usuario u : usuarios){
      cmbUsuario.addItem(u);
    }
  }
  
  private void preencheComboReceita(){
    ListaReceita listaReceita = new ListaReceita();
    List<Receita> receitas = listaReceita.listar();
    
    for(Receita r : receitas){
      cmbReceita.addItem(r);
   }
  }
  

